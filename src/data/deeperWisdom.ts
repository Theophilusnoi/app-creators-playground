export const getDeepWisdomByCategory = (category: string): DeepWisdomConcept[] => {
  return Object.values(deeperWisdom).filter(concept => concept.category === category);
};

export const searchDeepWisdom = (query: string): DeepWisdomConcept[] => {
  const lowercaseQuery = query.toLowerCase();
  return Object.values(deeperWisdom).filter(concept =>
    concept.title.toLowerCase().includes(lowercaseQuery) ||
    concept.essence.toLowerCase().includes(lowercaseQuery) ||
    concept.hiddenTruths.some(truth => truth.toLowerCase().includes(lowercaseQuery)) ||
    concept.awakening_insights.some(insight => insight.toLowerCase().includes(lowercaseQuery))
  );
};
